#include <iostream>
using namespace std;
#include <vector>    // STL中每个容器要使用, 都要包含对应的头文件
#include <algorithm> // 这是标准算法的头文件

void myPrint(int val)
{
    cout << val << endl;
}

void test01()
{
    //创建一个vector容器, 数组
    vector<int> v;

    //向容器中插入数据
    v.push_back(10);
    v.push_back(20);
    v.push_back(30);
    v.push_back(40);

    //通过迭代器访问容器中的数据
    vector<int>::iterator itBegin = v.begin();
    vector<int>::iterator itEnd = v.end();

    //第一种遍历方式
    while (itBegin != itEnd)
    {
        cout << *itBegin << endl; //类似指针, 解引用, 取出值
        itBegin++;                //往后偏移
    }
    //第二种遍历方式
    for (vector<int>::iterator it = v.begin(); it != v.end(); it++)
    {
        cout << *it << endl;
    }
    //第三种遍历方式, 利用STL提供遍历算法
    for_each(v.begin(), v.end(), myPrint);
}

int main()
{
    test01();

    system("pause");

    return 0;
}
